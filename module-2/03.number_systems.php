<?php
$num = 123;     // decimal
$oct = 012;     // octal
$bi = 110;     // binary
$hex = 0x8d;    // hexadecimal

printf("decimal number = %d\n", $num);
printf("octal number = %o\n", $oct);
printf("binary number = %b\n", $bi);
printf("hexadecimal number = %x\n", $hex);

// conversion
printf("binary equivalent of %d = %b\n", 12,12);
printf("octal equivalent of %d = %o\n", 12,12);
printf("hexadecimal equivalent of %d = %x\n", 12,12);




